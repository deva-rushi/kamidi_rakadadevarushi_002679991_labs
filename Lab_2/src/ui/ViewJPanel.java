    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        var selectedRow = tblVitals.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel tableModel = (DefaultTableModel) tblVitals.getModel();
        var vitals = (Vitals) tableModel.getValueAt(selectedRow, 0);
        vitalsHistory.deleteVitals(vitals);
        JOptionPane.showMessageDialog(this, "Vitals deleted");
        populateData();
    }//GEN-LAST:event_btnDeleteActionPerformed


