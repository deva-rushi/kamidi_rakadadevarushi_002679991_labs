    private void populateData() {
        DefaultTableModel tableModel = (DefaultTableModel) tblVitals.getModel();
        tableModel.setRowCount(0);
        for(Vitals vitals: vitalsHistory.getVitalsHistory()){
            Object[] row = new Object[3];
            row[0] = vitals;
            row[1] = vitals.getBloodPressure();
            row[2] = vitals.getPulse();
            tableModel.addRow(row);
        }
    }
