    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        var selectedRow = tblVitals.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to view");
        }
        DefaultTableModel tableModel = (DefaultTableModel) tblVitals.getModel();
        var vitals = (Vitals) tableModel.getValueAt(selectedRow, 0);
        txtTemp.setText(String.valueOf(vitals.getTemperature()));
        txtBP.setText(String.valueOf(vitals.getBloodPressure()));
        txtPulse.setText(String.valueOf(vitals.getPulse()));
        txtDate.setText(vitals.getDate());
    }//GEN-LAST:event_btnViewActionPerformed

