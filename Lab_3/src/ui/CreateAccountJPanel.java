    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(fieldsValidation()){
            Account account = accountDirectory.addAccount();
            account.setRoutingNumber(txtRoutingNumber.getText());
            account.setAccountNumber(txtAccNumber.getText());
            account.setBankName(txtBankName.getText());
            account.setAccountBalance(Integer.parseInt(txtBalance.getText()));
            JOptionPane.showMessageDialog(this, "Account created successfully");
        }
        else JOptionPane.showMessageDialog(this, "Please enter all the fields correctly!!!", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnCreateActionPerformed

