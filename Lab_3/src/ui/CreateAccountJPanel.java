   private boolean fieldsValidation() {
        return isValid("^[0-9]+$", txtRoutingNumber.getText()) && isValid("^[0-9]+$", txtAccNumber.getText()) && isValid("^[a-zA-Z]+$", txtBankName.getText()) && isValid("^[0-9]+$", txtBalance.getText());
    }
    
    public static boolean isValid(String regex, String input) {
        return Pattern.matches(regex, input);
    }
