    private boolean fieldsValidation() {
        return isValid("^[0-9]+$", txtRoutingNumber.getText()) && isValid("^[0-9]+$", txtAccNumber.getText()) && isValid("^[a-zA-Z]+$", txtBankName.getText());
    }
    
    public static boolean isValid(String regex, String input) {
        return Pattern.matches(regex, input);
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(fieldsValidation()){
            account.setRoutingNumber(txtRoutingNumber.getText());
            account.setAccountNumber(txtAccNumber.getText());
            account.setBankName(txtBankName.getText());
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Account details successfully updated");
            tableModel.fireTableDataChanged();
        }
        else JOptionPane.showMessageDialog(this, "Please enter correct account details", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnSaveActionPerformed


