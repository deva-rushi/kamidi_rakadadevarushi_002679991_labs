    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        workJPanel.remove(this);
        CardLayout layout = (CardLayout) workJPanel.getLayout();
        layout.previous(workJPanel);
        if (workJPanel.getComponent(workJPanel.getComponentCount()-1) instanceof ManageAccountJPanel) {
            ManageAccountJPanel managePanel = (ManageAccountJPanel) workJPanel.getComponent(workJPanel.getComponentCount()-1);
            managePanel.populateData();
        }
    }//GEN-LAST:event_btnBackActionPerformed

