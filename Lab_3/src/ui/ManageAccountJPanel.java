    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        var selectedRow = tblDetails.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to view", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DefaultTableModel tableModel = (DefaultTableModel) tblDetails.getModel();
        var account = (Account) tableModel.getValueAt(selectedRow, 0);
        ViewJPanel viewJPanel = new ViewJPanel(workJPanel, account, tableModel);
        workJPanel.add("ViewJPanel", viewJPanel);
        CardLayout layout = (CardLayout) workJPanel.getLayout();
        layout.next(workJPanel);
    }//GEN-LAST:event_btnViewActionPerformed
    public static boolean isValid(String regex, String input) {
        return Pattern.matches(regex, input);
    }
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(!txtSearch.getText().equals("") || isValid("^[0-9]+$", txtSearch.getText())){
            var account = accountDirectory.searchAccount(txtSearch.getText());
            if(account == null) JOptionPane.showMessageDialog(this, "Account number doesn't exist!!!", "Warning", JOptionPane.WARNING_MESSAGE);
            else{
                DefaultTableModel tableModel = (DefaultTableModel) tblDetails.getModel();
                ViewJPanel viewJPanel = new ViewJPanel(workJPanel, account, tableModel);
                workJPanel.add("ViewJPanel", viewJPanel);
                CardLayout layout = (CardLayout) workJPanel.getLayout();
                layout.next(workJPanel);
            }
        }
        else JOptionPane.showMessageDialog(this, "Please enter correct account number!!!", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        var selectedRow = tblDetails.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete!!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DefaultTableModel tableModel = (DefaultTableModel) tblDetails.getModel();
        var details = (Account) tableModel.getValueAt(selectedRow, 0);
        accountDirectory.deleteAccount(details);
        JOptionPane.showMessageDialog(this, "Account deleted!!!");
        populateData();
    }//GEN-LAST:event_btnDeleteActionPerformed


