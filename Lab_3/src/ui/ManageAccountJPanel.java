    public void populateData() {
            DefaultTableModel tableModel = (DefaultTableModel) tblDetails.getModel();
            tableModel.setRowCount(0);
            for(Account account: accountDirectory.getAccounts()){
                Object[] row = new Object[4];
                row[0] = account;
                row[1] = account.getRoutingNumber();
                row[2] = account.getAccountNumber();
                row[3] = account.getAccountBalance();
                tableModel.addRow(row);
            }
        }
    
